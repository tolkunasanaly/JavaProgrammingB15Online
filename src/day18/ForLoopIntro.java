package day18;

public class ForLoopIntro {

    public static void main(String[] args) {

        //count 1 to 5

        for( int i=1 ; i<=5 ; i++ ) {
            System.out.println("i = " +i);
            System.out.println("Happy Birthday Roksana!");
        }

        for (int counter = 5; counter>0; counter --){

            System.out.println("counter " +counter);
        }

            }

}
